    @Override
    public void actualizar(AModel amodel) {
        GestorClientes gestor = (GestorClientes) amodel;
        int totalHombres = gestor.getTotalHombres();
        int totalMujeres = gestor.getTotalMujeres();
        graficar(totalHombres, totalMujeres);
    }
    
     private void graficar(int totalHombres, int totalMujeres)
    {
        //Total de personas, hombre y mujeres.
        int totalPersonas = totalHombres + totalMujeres;
        
        //Obtenemos los porcentajes con rexpecto a los hombres y las mujeres
        this.porcentajeHombres = (100 * ((float)(totalHombres))) / ((float)(totalPersonas));
        this.porcentajeMujeres = (100 * ((float)(totalMujeres))) / ((float)(totalPersonas));
        
        //En estas variables calculamos el porcentaje que hay que pintar en los rectangulos
        this.altoHombres = (((int)(porcentajeHombres)) * alto) / 100; 
        this.altoMujeres = (((int)(porcentajeMujeres)) * alto) / 100; 
        
        Graphics g = myPanel.getGraphics();
        //Implementamos un metodo que dibuje las graficas de porcentajes
        paintComponent(g);
     
    }
    
    //Metodo de dibujo
    @SuppressWarnings("empty-statement")
    public void paintComponent(Graphics g )
    {
        super.paintComponents(g);;
        
        g.setColor(Color.black);
        g.drawRect(60, 60, ancho, alto);
        g.drawRect(260, 60, ancho, alto);
        
        g.drawString("Hombres", 65, 345);
        g.drawString("Mujeres", 265, 345);
        g.drawString("" + porcentajeHombres +" %", 90, 55);
        g.drawString("" + porcentajeMujeres + " %", 290, 55);
       
        g.setColor(Color.RED);
        g.fillRect(61, 60 + (alto - altoHombres), ancho-1, altoHombres);
        g.fillRect(261, 60 + (alto- altoMujeres), ancho-1, altoMujeres);
    }

